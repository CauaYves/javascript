let num = [5, 1, 6, 0]
num[4] = 7  // adicionando um valor na array.
num.push(8)  // adicionando o valor na última posição da array.
console.log(`O comprimento da array é ${num.length}`)  // retornando o comprimento da array.
console.log(`O primeiro valor da array é ${num[0]}`)  // exibindo o primeiro valor da array no console.
num.sort() // colocando a array em ordem crescente
console.log(`A array está em ordem crescente ${num} `)